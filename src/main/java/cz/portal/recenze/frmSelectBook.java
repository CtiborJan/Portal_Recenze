    private void lstKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lstKeyPressed
        if (evt.getKeyCode()==13)
            send_data();
    }//GEN-LAST:event_lstKeyPressed

    private void lstMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2)
            this.send_data();
    }//GEN-LAST:event_lstMouseClicked

    private void txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKeyPressed
        // TODO add your handling code here:
        if (evt.isControlDown()==true && evt.getKeyCode()==10)
        {
            if (this.lstModel.size()>0)
            {
                this.lst.setSelectedIndex(0);
                this.send_data();
            }
        }
    }//GEN-LAST:event_txtKeyPressed
    private void send_data()
    {
        int i=this.lst.getSelectedIndex();
        if (i==-1 && books.size()>0)
            i=0;
        if (i>-1)
        {
            clsReviewMaker.set_book(books.get(i));
            this.setVisible(false);
        }
    }
   

