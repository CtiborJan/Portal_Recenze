    private void txtChanged()
    {
        String t=txt.getText();
        this.lstModel.clear();
        if (t.length()>=3)
        {
            
            books=Recenze.feed.find_books(txt.getText());

            for (clsBook b : books)
            {
                this.lstModel.addElement(b.kod + ": " + b.name+" ("+b.autor+")");
            }
        }
    }
